package com.ivan.learning.codetest.dto;

import org.springframework.stereotype.Component;

@Component
public class PetDTO {
    private String name;
    private String type;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}